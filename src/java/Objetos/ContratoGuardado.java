public class ContratoGuardado {
    
    
    private int idContratoGuardado, idContrato, idEstado;
    private String nombreEstado;

    public int getIdContratoGuardado() {
        return idContratoGuardado;
    }

    public void setIdContratoGuardado(int idContratoGuardado) {
        this.idContratoGuardado = idContratoGuardado;
    }

    public int getIdContrato() {
        return idContrato;
    }

    public void setIdContrato(int idContrato) {
        this.idContrato = idContrato;
    }

    public int getIdEstado() {
        return idEstado;
    }

    public void setIdEstado(int idEstado) {
        this.idEstado = idEstado;
    }

    public String getNombreEstado() {
        return nombreEstado;
    }

    public void setNombreEstado(String nombreEstado) {
        this.nombreEstado = nombreEstado;
    }
    
    
    
    
}
